    public function store(Request $request)
    {
        $validated = $request->validate([
            'greeting' => 'nullable|string|max:10',
            'name' => 'required|string|max:255',
            'identification_number' => 'nullable|string|max:20',
            'email' => 'nullable|email|unique:contacts,email',
            'phone' => 'nullable|string|max:20',
            'position' => 'nullable|string|max:255',
            'photo' => 'nullable|image|max:2048',
        ]);

        if ($request->hasFile('photo')) {
            $validated['photo_path'] = $request->file('photo')->store('contacts', 'public');
        }

        Contact::create($validated);

        return redirect()->route('contacts.index')->with('success', 'Contacto creado exitosamente.');
    }

